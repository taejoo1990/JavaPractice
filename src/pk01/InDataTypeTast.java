package pk01;

public class InDataTypeTast {

	public static void main(String[] args) {
		byte aVar=100; // 8Bit=1Byte
		short bVar=30000; //16Bit=2Byte
		int cVar=50000; //32Bit= 4Byte
		long dVar=1000000; //64Bit=8Byte
		
		System.out.println("byte 타입 : " + aVar);
		System.out.println("short 타입 : " + bVar);
		System.out.println("int 타입 : " + cVar);
		System.out.println("long 타입 : " + dVar);

	}

}
