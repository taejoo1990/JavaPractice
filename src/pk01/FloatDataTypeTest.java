package pk01;

public class FloatDataTypeTest {

	public static void main(String[] args) {
		float aVar;
		double bVar;
		
		//aVar=10.2;
		aVar=20.1f;
		bVar=20.1;
		System.out.println("float 타입 : " + aVar);
		System.out.println("double 타입 : " + bVar);
		
		double dnum=3.14;
		float fnum=3.14f;
		
		System.out.println(dnum);
		System.out.println(fnum);

	}

}
